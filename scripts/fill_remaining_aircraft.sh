# هياكل الطائرات Aircraft Structures

## مقدمة

تعتبر هياكل الطائرات من أهم مجالات هندسة الطيران، حيث يجب أن تحقق الطائرة التوازن بين القوة والوزن والكفاءة الهوائية.

يتكون الهيكل من:

- جسم الطائرة Fuselage
- الأجنحة Wings
- الذيل Empennage
- معدات الهبوط Landing Gear Structure

---

# جسم الطائرة Fuselage

وظائفه:

- حمل الركاب والبضائع.
- تثبيت الأنظمة الداخلية.
- تحمل الضغوط أثناء الطيران.

يتعرض جسم الطائرة إلى:

- ضغط المقصورة.
- الأحمال الهوائية.
- الاهتزازات.

---

# الأجنحة Wings

الجناح هو العنصر الأساسي المسؤول عن:

- توليد الرفع.
- حمل خزانات الوقود.
- تثبيت أسطح التحكم.

مكونات الجناح:

- Spars العوارض الرئيسية.
- Ribs الأضلاع.
- Skin الغلاف الخارجي.

---

# الذيل Empennage

يشمل:

- Horizontal Stabilizer.
- Vertical Stabilizer.
- Elevator.
- Rudder.

وظيفته تحقيق استقرار الطائرة.

---

# مواد تصنيع الطائرات

## سبائك الألومنيوم Aluminum Alloys

تستخدم بسبب:

- خفة الوزن.
- مقاومة التآكل.
- سهولة التصنيع.

---

## التيتانيوم Titanium

يستخدم في:

- أجزاء المحركات.
- المناطق ذات الحرارة العالية.

مميزاته:

- قوة عالية.
- مقاومة الحرارة.

---

## المواد المركبة Composite Materials

مثل:

Carbon Fiber

مميزاتها:

- وزن منخفض.
- صلابة عالية.
- مقاومة التعب.

---

# الأحمال والإجهاد

تتعرض الطائرة إلى:

- Tensile Stress إجهاد الشد.
- Compression Stress إجهاد الضغط.
- Shear Stress إجهاد القص.

---

# التعب المعدني Fatigue

يحدث بسبب تكرار الأحمال خلال:

- الإقلاع.
- الهبوط.
- تغير الضغط.

يتم التحكم به بواسطة:

- الفحص الدوري.
- اختبارات NDT.

---

# الاختبارات غير الإتلافية NDT

تشمل:

- Ultrasonic Testing.
- X-Ray Inspection.
- Eddy Current Testing.

تستخدم لاكتشاف:

- الشقوق.
- العيوب الداخلية.

---

# مستقبل هياكل الطائرات

يتجه التطوير نحو:

- المواد الذكية.
- الطباعة ثلاثية الأبعاد.
- الهياكل خفيفة الوزن.

